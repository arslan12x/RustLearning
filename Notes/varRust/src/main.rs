fn main() {

// Scalar variables- These are those variables in whivh only one value can store.
// A scalar type represents a single value.

    let a1=35;
    
    let a2:i64 =34;
    
    let a3:f32 = 1.0;

    let a4:bool=true;

    let a5:char = 'h';

    println!("{}",a1);
    println!("{}",a2);
    println!("{}",a3);
    println!("{}",a4);
    println!("{}",a5);

// Constants are declared using the const keyword while variables are declared using the let keyword.

    const a6:i32=1000;
    println!("{}",a6); 
    
}
